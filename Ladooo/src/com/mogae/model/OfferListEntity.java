package com.mogae.model;

public class OfferListEntity {
	private String offerName;
	 private String disciption;
	 private String offerMoney;
	 private String offerStatus;
	 private String offerImage;
	public String getOfferName() {
		return offerName;
	}
	public void setOfferName(String offerName) {
		this.offerName = offerName;
	}
	public String getDisciption() {
		return disciption;
	}
	public void setDisciption(String disciption) {
		this.disciption = disciption;
	}
	public String getOfferMoney() {
		return offerMoney;
	}
	public void setOfferMoney(String offerMoney) {
		this.offerMoney = offerMoney;
	}
	public String getOfferStatus() {
		return offerStatus;
	}
	public void setOfferStatus(String offerStatus) {
		this.offerStatus = offerStatus;
	}
	public String getOfferImage() {
		return offerImage;
	}
	public void setOfferImage(String offerImage) {
		this.offerImage = offerImage;
	}
	 
}
