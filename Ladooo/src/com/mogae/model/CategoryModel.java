package com.mogae.model;

public class CategoryModel {

	private int cat_id;
	private String cat_name;
	private String num_of_rounds;
	private String cat_vis;
	public int getCat_id() {
		return cat_id;
	}
	public void setCat_id(int cat_id) {
		this.cat_id = cat_id;
	}
	public String getCat_name() {
		return cat_name;
	}
	public void setCat_name(String cat_name) {
		this.cat_name = cat_name;
	}
	public String getCat_vis() {
		return cat_vis;
	}
	public void setCat_vis(String cat_vis) {
		this.cat_vis = cat_vis;
	}
	public String getNum_of_rounds() {
		return num_of_rounds;
	}
	public void setNum_of_rounds(String num_of_rounds) {
		this.num_of_rounds = num_of_rounds;
	}
	
	
}
